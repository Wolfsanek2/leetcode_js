const { nearestExit } = require('.');

test('maze = [["+","+",".","+"],[".",".",".","+"],["+","+","+","."]], entrance = [1,2] => 1', () => {
	expect(
		nearestExit(
			[
				['+', '+', '.', '+'],
				['.', '.', '.', '+'],
				['+', '+', '+', '.'],
			],
			[1, 2],
		),
	).toEqual(1);
});

test('maze = [["+","+","+"],[".",".","."],["+","+","+"]], entrance = [1,0] => 2', () => {
	expect(
		nearestExit(
			[
				['+', '+', '+'],
				['.', '.', '.'],
				['+', '+', '+'],
			],
			[1, 0],
		),
	).toEqual(2);
});

test('maze = [[".","+"]], entrance = [0,0] => -1', () => {
	expect(nearestExit([['.', '+']], [0, 0])).toEqual(-1);
});
