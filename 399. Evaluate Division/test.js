const { calcEquation } = require('.');

test('equations = [["a","b"],["b","c"]], values = [2.0,3.0], queries = [["a","c"],["b","a"],["a","e"],["a","a"],["x","x"]] => [6.00000,0.50000,-1.00000,1.00000,-1.00000]', () => {
	expect(
		calcEquation(
			[
				['a', 'b'],
				['b', 'c'],
			],
			[2.0, 3.0],
			[
				['a', 'c'],
				['b', 'a'],
				['a', 'e'],
				['a', 'a'],
				['x', 'x'],
			],
		),
	).toEqual([6.0, 0.5, -1.0, 1.0, -1.0]);
});

test('equations = [["a","b"],["b","c"],["bc","cd"]], values = [1.5,2.5,5.0], queries = [["a","c"],["c","b"],["bc","cd"],["cd","bc"]] => [3.75000,0.40000,5.00000,0.20000]', () => {
	expect(
		calcEquation(
			[
				['a', 'b'],
				['b', 'c'],
				['bc', 'cd'],
			],
			[1.5, 2.5, 5.0],
			[
				['a', 'c'],
				['c', 'b'],
				['bc', 'cd'],
				['cd', 'bc'],
			],
		),
	).toEqual([3.75, 0.4, 5.0, 0.2]);
});

test('equations = [["a","b"]], values = [0.5], queries = [["a","b"],["b","a"],["a","c"],["x","y"]] => [0.50000,2.00000,-1.00000,-1.00000]', () => {
	expect(
		calcEquation(
			[['a', 'b']],
			[0.5],
			[
				['a', 'b'],
				['b', 'a'],
				['a', 'c'],
				['x', 'y'],
			],
		),
	).toEqual([0.5, 2.0, -1.0, -1.0]);
});
